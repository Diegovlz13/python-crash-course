coches = ['bmw', 'audi', 'toyota', 'subaru']
print("Coches original:", coches)
coches.sort()
print("Coches ordenados permanentemente:", coches)
coches.sort(reverse=True)
print("Coches ordenados inversamente permanentemente:", coches)
coches.reverse()
print("Coches invertidos:", coches)
print("El numero de coches es:", len(coches))