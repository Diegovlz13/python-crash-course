nombre = "\t\n   Diego Velazquez   \t\t"
print(nombre)

# Sirve para eliminar carácteres a la derecha de la cadena
print(nombre.rstrip())
# Sirve para eliminar carácteres a la izquierda de la cadena
print(nombre.lstrip())
# Sirve para eliminar carácteres en ambos lados de la cadena
print(nombre.strip())